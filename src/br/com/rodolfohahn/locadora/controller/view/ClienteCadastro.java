    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        Cliente c = new Cliente();
        if (!(txId.getText().equals("") || (txId.getText().equals(null)))) {
            c.setId(Integer.parseInt(txId.getText()));
        }
        c.setNome(txNome.getText());
        c.setTelFixo(txTelFixo.getText());
        c.setTelCel(txTelCel.getText());
        if (!(txSalario.getText().equals("") || (txSalario.getText().equals(null)))) {
            c.setSalarioClie(Double.parseDouble(txSalario.getText()));
        }
        c.setCpf(txCpf.getText());
        c.setRg(txRg.getText());
        c.setProfissao(txProfissao.getText());
        if (rbFeminino.isSelected()){
            c.setSexo("Feminino");
        } else if (rbMasculino.isSelected()) {
            c.setSexo("Masculino");
        }
        try {
            String data = txDataNascimento.getText();
            c.setDataNascimento(new SimpleDateFormat("dd/MM/yyyy").parse(data));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "erro ao converter a data");
        }

        ClienteController cc = new ClienteController();

        if (c.getId() == 0) {
            int id = cc.salvar(c);

        } else {
            int idCliente = cc.salvar(c);
            if (idCliente > 0) {
                modelo.removeRow(linhaSelecionada);

            }
        }
        //limpar campos

        dispose();
    }//GEN-LAST:event_btSalvarActionPerformed

