    public ClienteCadastro(DefaultTableModel modelo, int linhaSelecionada, int idCliente) {
        this.linhaSelecionada = linhaSelecionada;
        this.modelo = modelo;
        initComponents();
        ClienteController cc = new ClienteController();
        Cliente c = cc.listById(idCliente);
        
        txId.setText(Integer.toString(c.getId()));
        txNome.setText(c.getNome());
        txTelFixo.setText(c.getTelFixo());
        txTelCel.setText(c.getTelCel());
        txCpf.setText(c.getCpf());
        txRg.setText(c.getRg());
        txSalario.setText(Double.toString(c.getSalarioClie()));
        txDataNascimento.setText("");
        txProfissao.setText(c.getProfissao());
        if (c.getSexo().equals("Feminino")) {
            rbFeminino.setSelected(true);
        } else if (c.getSexo().equals("Masculino")) {
            rbMasculino.setSelected(true);
        }
    }


