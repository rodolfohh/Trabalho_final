    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       Filme f = new Filme();
       f.setNomeFilme((txNomeFilme.getText()));
       
       try {
                        String data = txDataLancamento.getText();
                        f.setDataLancamento(new SimpleDateFormat("dd/MM/yyyy").parse(data));
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Data incorreta" + e);
                    }
       f.setId(Integer.parseInt(txId.getText()));
       f.setCategoria((CategoriaFilme) cbCategoria.getSelectedItem());
       
       FilmeController fc= new FilmeController();
       if(!(txId.getText().equals("")||(txId.getText().equals(null)))){
           f.setId(Integer.parseInt(txId.getText()));
           if(fc.salvar(f)>0){
               modelo.removeRow(linhaSelecionada);
               modelo.addRow(new Object[]{f.getId(),f.getNomeFilme(),f.getDuracaoFilme()});
               JOptionPane.showMessageDialog(null, "Filme alterado com sucesso!");
           }
       }else{
           f.setId(Integer.parseInt(txId.getText()));
       int id = fc.salvar(f);
       if(id > 0){
          modelo.addRow(new Object[]{id,//Valor da variável que o método salvar retorna 
              f.getNomeFilme(),
              f.getDuracaoFilme()});
           JOptionPane.showMessageDialog(null,"Cadastro realizado com sucesso");
       }
       }
       dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

