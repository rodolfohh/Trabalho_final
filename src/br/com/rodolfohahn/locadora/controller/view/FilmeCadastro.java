private void carregarCombo() {
        
        DefaultComboBoxModel comboModel = (DefaultComboBoxModel) cbCategoria.getModel();
        cbCategoria.removeAllItems();
        
        List<CategoriaFilme> categorias = new ArrayList<>();
        CategoriaController cc = new CategoriaController();
        categorias =cc.ListarTodos();
        
        for(int linha = 0 ; linha<categorias.size(); linha++){
            CategoriaFilme cf = categorias.get(linha);
            comboModel.addElement(cf);
        }
    }

    private void carregarCombo(CategoriaFilme categoria) {
        
        DefaultComboBoxModel comboModel = (DefaultComboBoxModel) cbCategoria.getModel();
        cbCategoria.removeAllItems();
        
        List<CategoriaFilme> categorias = new ArrayList<>();
        CategoriaController cc = new CategoriaController();
        categorias =cc.ListarTodos();
        
        for(int linha = 0 ; linha<categorias.size(); linha++){
            CategoriaFilme cf = categorias.get(linha);
            comboModel.addElement(cf);
        if(categoria.getId() == cf.getId()){
            comboModel.setSelectedItem(cf);
        }
        }
    }
