    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        
        CategoriaFilme cf = new CategoriaFilme();
        if (!(txId.getText().equals("") || (txId.getText().equals(null)))) {
            cf.setId(Integer.parseInt(txId.getText()));
        }
        cf.setNome_categoria(txCategoria.getText());


        CategoriaController cc = new CategoriaController();
        if (cf.getId() >= 0) {
            int id = cc.salvar(cf);

            JOptionPane.showMessageDialog(null, "cadastrado com sucesso1");


        } else {
            int id = cc.salvar(cf);
            if (id > 0) {
                modelo.removeRow(linhaSelecionada);
                JOptionPane.showMessageDialog(null, "cadastrado com sucesso2");


            }
        }
       
        limparcampos();

        //dispose();
    }//GEN-LAST:event_btSalvarActionPerformed
